// TypeScript Arrays - there are two ways to declare arrays in TypeScript:
// string[] or Array<string>

// Numbers array
let numbers: number[] = [1, 2, 3];
// a number
let myNumber = numbers[1];

// booleans array
let bools: boolean[] = [true, false];

// string array
let strings: string[] = ['a', 'b', 'c'];
// inferred type
let values = strings;
